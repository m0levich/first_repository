package lesson_1.ITC_1;

public class Task_3 {
    public static void main(String[] args) {

        for (int i = 1; i < 11; i++){
            for (int j = 1; j < 11; j++){
                System.out.println(i + " X " + j + " = " + (j * i));
            }
            System.out.println();
        }
    }
}
